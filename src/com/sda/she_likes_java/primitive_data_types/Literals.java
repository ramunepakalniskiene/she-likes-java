package com.sda.she_likes_java.primitive_data_types;

public class Literals {
    public static void main(String[] args) {
        // literal is direct value
        //String literal is "name"
        String name = "Mariusz";

        // integer literal
        int age = 40; // 40 is type of int

        // long literal
        long anotherAge = 30L;

        // double literal
        double doubleNumber=3.1;

        //float literal;
        float floatNumber=3.1F;
    }
}
